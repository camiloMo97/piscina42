int	ft_atoi(char *str)
{
	int	x;
	int	n;
	int	sig;

	x = 0;
	n = 0;
	sig = 1;
	while ((str[x] > 10 && str[x] < 14) || str[x] == 32)
		x++;
	if (str[x] == '+' || str[x] == '-')
	{
		if (str[x] == '-')
			sig = sig * -1;
		x++;
	}
	while (str[x] > 47 && str[x] < 58)
	{
		n = (n * 10) + (str[x] - '0');
		x++;
	}
	return (n * sig);
}